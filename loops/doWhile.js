// do-while loop
let i=4;

do{
console.log(`${i}: see? output is being printed atleast once`);
i++;
}while(i<4);
console.log("\n");


// while loop in case of array
let friends=["Sid","abhi","akash","sushant"];

let j=0;
do
{
    console.log(`${friends[j]} is my friend.`);
    j++;
}
while(j<friends.length);